const mongoose = require("mongoose");

const CafeSchema = new mongoose.Schema({
  cafeName: {
    type: String,
    required: true,
    trim: true,
  },
  contact: {
    type: String,
    required: true,
    trim: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model("Cafe", CafeSchema);
